#1) Verificação de números diferentes Peça dois números ao usuário. Se forem diferentes, exiba: Os números são diferentes, caso contrário, exiba: Os números são iguais.

num1 = float(input("Ensira um número"))
num2 = float(input("Ensira outro número"))

if num1 != num2:
    print("Os números são diferentes.")
else:
    print("Os números são iguais.")


